        <h2>¿Durante cuánto tiempo conservamos sus datos?</h2>
        
        <p>Los datos personales serán conservados durante el tiempo que dure la relación con Agrodoc y/o para alcanzar la finalidad de tratamiento perseguida o, en su caso, hasta que ejerza sus derechos para terminar el tratamiento.</p>
        
